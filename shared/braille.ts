export const KANA_TO_BRAILLE: Record<string, string> = {
  // あ行
  あ: "⠁",
  い: "⠃",
  う: "⠉",
  え: "⠋",
  お: "⠊",

  // か行
  か: "⠡",
  き: "⠣",
  く: "⠩",
  け: "⠫",
  こ: "⠪",

  // さ行
  さ: "⠱",
  し: "⠳",
  す: "⠹",
  せ: "⠻",
  そ: "⠺",

  // た行
  た: "⠕",
  ち: "⠗",
  つ: "⠝",
  て: "⠟",
  と: "⠞",

  // な行
  な: "⠅",
  に: "⠇",
  ぬ: "⠍",
  ね: "⠏",
  の: "⠎",

  // は行
  は: "⠥",
  ひ: "⠧",
  ふ: "⠭",
  へ: "⠯",
  ほ: "⠮",

  // ま行
  ま: "⠵",
  み: "⠷",
  む: "⠽",
  め: "⠿",
  も: "⠾",

  // や行
  や: "⠌",
  ゆ: "⠬",
  よ: "⠜",

  // ら行
  ら: "⠑",
  り: "⠓",
  る: "⠙",
  れ: "⠛",
  ろ: "⠚",

  // わ行
  わ: "⠄",
  を: "⠔",
  ん: "⠴",

  っ: "⠂", // 促音符

  ー: "⠒",
};

export function toBraille(input: string): string {
  // 1文字ずつ変換。対応してない文字はそのまま返す。
  return [...input].map((ch) => KANA_TO_BRAILLE[ch] ?? ch).join("");
}
